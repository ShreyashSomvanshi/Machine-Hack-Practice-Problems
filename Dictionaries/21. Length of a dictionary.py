'''
Consider a dictionary as dict1 consisting of n-number of keys and corresponding values. Find the length of the dictionary.

Example 1: 
Input:  dict1 = {"a","b","c"}
Output: 3
 
Example 2: 
Input: dict1 = {"a":1,"b":2,"c":3,"d":4}
Output: 4
'''

# Solution:
def lengthOfDictionary(dict1):
  return len(dict1)
  
print(lengthOfDictionary(dict1))

